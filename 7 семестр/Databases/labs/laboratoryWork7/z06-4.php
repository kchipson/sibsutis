	<?php
		$size = 30;

		$colors = array("white","aqua","blue","yellow","purple","red","lime");

		for($k=4;$k<=7;$k++){
			print "<h1>k = $k</h1>";
			print "<table cellpadding=0 border=1>";
			for($i=1;$i<=$size;$i++){
				print "<tr>";
				for($j=1;$j<=$size;$j++){
					$ost = $i*$j%$k;
					print "<td style=\"background-color:$colors[$ost];\" width=\"14px\" height=\"15px\"><font size=\"1\">&nbsp;</font></td>";
				}
				print "</tr>";
			}
			print "</table>";
		}

	?>
