2. Используя таблицу Пифагора из предыдущего скрипта, добавьте еще один внешний цикл, его индекс k — это число (от 4 до 7), в зависимости от остатков от деления на которое ячейки будут раскрашиваться в цвета из массива $colors[]. Таким образом, должно получиться 4 таблицы-мозаики. Перед каждой таблицей выведите чему равно k (т.е. на что мы делим).
